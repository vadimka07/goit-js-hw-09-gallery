const gallery = document.querySelector( '.js-gallery' );
const popup = document.querySelector( '.js-lightbox' );
let originalImage = document.querySelector( '.lightbox__image' );
const buttonClose = document.querySelector( 'button[data-action="close-lightbox"]' );
const overlayClosePopup = document.querySelector( '.lightbox__overlay' );
const galleryElement = galleryItems.map( ( item ) => {
  return `<li class='gallery__item'> <a class='gallery__link' href='#'><img data-src='${ item.original }' class='gallery__image' src='${ item.preview }' alt='${ item.description }' width='392'></a>  </li>`;
} );
gallery.insertAdjacentHTML( 'afterbegin', galleryElement.join( '' ) );

gallery.addEventListener( 'click', ( e ) => {
  if ( e.target.nodeName !== 'IMG' ) {
    return;
  }
  popup.classList.add( 'is-open' );
  document.body.classList.add( 'body__hidden' );
  originalImage.src = e.target.dataset.src;
} );

function closePopup() {
  popup.classList.remove( 'is-open' );
  document.body.classList.remove( 'body__hidden' );
  originalImage.src = '';
}

buttonClose.addEventListener( 'click', closePopup );

overlayClosePopup.addEventListener( 'click', closePopup );
